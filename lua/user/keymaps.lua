local opts = { noremap = true, silent = true }

-- Shorten function name
local map = vim.api.nvim_set_keymap

--Remap space as leader key
map("", "<Space>", "<Nop>", opts)
vim.g.mapleader = " "
vim.g.maplocalleader = " "

-- Modes
--   normal_mode = "n",
--   insert_mode = "i",
--   visual_mode = "v",
--   visual_block_mode = "x",
--   term_mode = "t",
--   command_mode = "c",

-- Normal --
-- Better window navigation
map("n", "<C-h>", "<C-w>h", opts)
map("n", "<C-j>", "<C-w>j", opts)
map("n", "<C-k>", "<C-w>k", opts)
map("n", "<C-l>", "<C-w>l", opts)

-- Resize with arrows
map("n", "<C-Up>", ":resize +2<CR>", opts)
map("n", "<C-Down>", ":resize -2<CR>", opts)
map("n", "<C-Left>", ":vertical resize -2<CR>", opts)
map("n", "<C-Right>", ":vertical resize +2<CR>", opts)

-- Navigate buffers
map("n", "<S-l>", ":bnext<CR>", opts)
map("n", "<S-h>", ":bprevious<CR>", opts)
map("n", "<leader>c", ":bdelete<CR>", opts)

-- Move text up and down
map("n", "<A-j>", "<Esc>:m .+1<CR>==gi", opts)
map("n", "<A-k>", "<Esc>:m .-2<CR>==gi", opts)

-- Insert --
-- Press kj fast to exit insert mode
map("i", "kj", "<ESC>", opts)

-- Visual --
-- Stay in indent mode
map("v", "<", "<gv", opts)
map("v", ">", ">gv", opts)

-- Move text up and down
map("v", "<A-j>", ":m .+1<CR>==", opts)
map("v", "<A-k>", ":m .-2<CR>==", opts)
map("v", "p", '"_dP', opts) --doesnot repalce value when pasting on top of a word

-- Visual Block --
-- Move text up and down
map("x", "J", ":move '>+1<CR>gv-gv", opts)
map("x", "K", ":move '<-2<CR>gv-gv", opts)
map("x", "<A-j>", ":move '>+1<CR>gv-gv", opts)
map("x", "<A-k>", ":move '<-2<CR>gv-gv", opts)

--Toggle plugins
-- Toggle nvim-tree
map('n', 'n', ":NvimTreeToggle <cr>", opts)

--toggle indentLine
map('n', '<leader>ti', ":IndentLinesToggle <cr>", opts)

--toggle Tagbar
map('n', '<leader>tt', ":TagbarToggle <cr>", opts)

--Toggle Telescope. fuzzy finder
map('n', '<leader>ff', ":Telescope find_files <cr>", opts)
map('n', '<leader>lg', ":Telescope live_grep<cr>", opts)
map('n', '<leader>e', "<Esc><Esc>", {});

--[[ Format Lsp ]]
map('n', '<leader><leader>f', ':Format<CR>', opts)


--[[ for lazygit ]]
map('n','<leader>g',':lua _LAZYGIT_TOGGLE()<CR>',opts)
